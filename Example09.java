public class Example09 {
    public static void main(String [] args) {
        int grade = 90;
        if (grade > 80) {
            System.out.println("成绩为优秀");
        } else if (grade > 70) {
            System.out.println("成绩为良好");
        } else if (grade > 60) {
            System.out.println("成绩为中等");
        } else {
            System.out.println("成绩为差等");
        }
    }
}
